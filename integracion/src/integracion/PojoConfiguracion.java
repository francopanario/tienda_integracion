package integracion;

public class PojoConfiguracion {
	
	String ip;
	String puerto;
	String usuario;
	String password;
	String rutaOrigenArchivo;
	String nombreArchivo;
	String urlChequearStock;
	String urlEnviarCompra;
	
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getPuerto() {
		return puerto;
	}
	public void setPuerto(String puerto) {
		this.puerto = puerto;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getRutaOrigenArchivo() {
		return rutaOrigenArchivo;
	}
	public void setRutaOrigenArchivo(String rutaOrigenArchivo) {
		this.rutaOrigenArchivo = rutaOrigenArchivo;
	}
	public String getNombreArchivo() {
		return nombreArchivo;
	}
	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}
	public String getUrlChequearStock() {
		return urlChequearStock;
	}
	public void setUrlChequearStock(String urlChequearStock) {
		this.urlChequearStock = urlChequearStock;
	}
	public String getUrlEnviarCompra() {
		return urlEnviarCompra;
	}
	public void setUrlEnviarCompra(String urlEnviarCompra) {
		this.urlEnviarCompra = urlEnviarCompra;
	}
	
	
}
